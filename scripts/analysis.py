def count_words(corpus):
    # считаем сколько раз встречается каждое слово во всех текстах
    counts = {}
    for doc in corpus:
        for w in doc:
            if w in counts:
                counts[w] = counts[w] + 1
            else:
                counts[w] = 1
    return counts


def top_words(corpus, top_n=20):
    # берём самые частые слова, сортируем по убыванию количества
    counts = count_words(corpus)
    pairs = list(counts.items())
    pairs.sort(key=lambda x: x[1], reverse=True)
    return pairs[:top_n]


def sentiment_score(words):
    # оценка от -1 до 1. если слов нет - 0
    if len(words) == 0:
        return 0.0
    pos = 0
    neg = 0
    for w in words:
        if w in positive_words:
            pos = pos + 1
        if w in negative_words:
            neg = neg + 1
    # +1 чтобы короткие тексты не давали сразу -1 или 1
    return (pos - neg) / (pos + neg + 1)


def sentiment_label(score, threshold=0.05):
    if score > threshold:
        return "позитив"
    if score < -threshold:
        return "негатив"
    return "нейтрал"


def analyze_documents(corpus, threshold=0.05):
    # для каждого текста считаем оценку и метку настроения
    result = []
    for doc in corpus:
        score = sentiment_score(doc)
        result.append({"score": round(score, 4),
                       "label": sentiment_label(score, threshold)})
    return result


def sentiment_summary(doc_sentiments):
    # сколько всего позитивных, нейтральных и негативных
    summary = {"позитив": 0, "нейтрал": 0, "негатив": 0}
    for d in doc_sentiments:
        summary[d["label"]] = summary[d["label"]] + 1
    return summary
