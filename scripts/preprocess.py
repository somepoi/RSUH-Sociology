def clean_text(text):
    if not isinstance(text, str):
        return ""
    text = text.lower()
    text = text.replace("ё", "е")
    # убираем ссылки
    text = re.sub(r"http\S+", " ", text)
    return text


def get_words(text):
    # берём из текста только русские слова
    text = clean_text(text)
    words = re.findall(r"[а-я]+", text)
    return words


def preprocess_text(text):
    # конвертим текст в список слов
    words = get_words(text)
    result = []
    for w in words:
        # приводим к начальной форме, если библиотека есть
        if morph is not None:
            try:
                w = morph.parse(w)[0].normal_form
            except Exception:
                pass  # если не получилось, оставляем как было
        # короткие слова и стоп-слова не берём
        if len(w) >= 3 and w not in stop_words:
            result.append(w)
    return result


def preprocess_corpus(texts):
    # обрабатываем сразу все тексты
    result = []
    for t in texts:
        result.append(preprocess_text(t))
    return result
